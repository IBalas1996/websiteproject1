
 function startLightBox(){
			var lbBg = document.getElementById("lightBoxBg");
			var lb = document.getElementById("lightBox");

			lbBg.style.display = "block";
			lb.style.display = "block";
	}

	function dismiss(){
		var lbBg = document.getElementById("lightBoxBg");
			var lb = document.getElementById("lightBox");
			lbBg.style.display = "none";
			lb.style.display = "none";
	}